        <h3>Quantified</h3>
        <h5>Studio leader: Adam Rule</h5>
        <h5>CogSci 272, Friday 08:50am - 10:00am</h5>
        <p>
          With the help of digital devices, we have an unprecedented ability to track our behaviors, habits, and thoughts. But turning data about ourselves and our communities into valuable insights is still remarkably difficult. This studio will explore ways to support monitoring, reflection, and behavior change by collecting, consuming, and sharing personally relevant information. We will consider:
          <ul>
            <li>How might we help people learn more about their everyday lives through self-tracking?</li>
            <li>How might we help people draw insights from the mass of data they collect about themselves?</li>
            <li>How might we encourage people to share information about themselves for the benefit of specific communities? (e.g. research, public health, support groups)</li>
          </ul>
          Check out <a href="http://www.personalinformatics.org" target="_blank">www.personalinformatics.org</a> and <a href="http://quantifiedself.com/" target="_blank">quantifiedself.com</a> for more inspiration
        </p>
