        <div class="col-lg-12 col-md-12">
          <h3>Cities</h3>
          <h5>Studio leader: Yasmine Kotturi</h5>
          <h5>Atkinson Hall 5204, Friday 12:50pm - 2:00pm <br> Atkinson Hall 5204, Friday 02:10pm - 3:20pm </h5>
          <p>
            Cities continue to grow in population all over the world. People move to densely populated areas to be inspired, find opportunity, and to be a part of cultural events. Such trends lead to massive amounts human activity, creativity, information and innovation, thus metamorphisizing cities into digital laboratories (see <a href="http://on.wsj.com/1TFznKL" target="_blank">http://on.wsj.com/1TFznKL</a>)
            <ul>
              <li>How can we leverage the effects of this massive influx of people to urban environments to strengthen connections between people? Or perhaps the opposite: find ways to enhance serenity in all the hussle and bussle that a city affords?</li>
              <li>How can we design applications that enhance the best parts of living in a city, or even minimize some of the disadvantages?</li>
              <li>How do cities inspire design? What can we learn from cities that can help us design successful online communities?</li>
            </ul>
            Taboos: Facebooks, WeChats, Venmos, Ubers...
          </p>
