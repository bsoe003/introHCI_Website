          <h3>Real Science Fiction</h3>
          <h5>Studio leader: Jasmine Roberts</h5>
          <h5>HSS 1346, Friday 10:10am - 11:20am<br>HSS 1346, Friday 11:30am - 12:40pm</h5>
          <a href="">Week 1 Slides</a> <br><a href="">Week 2 Slides</a><br><a href="">UI Workshop Slides</a>
