          <p>
            My Charizard for your Dragonite. That for this. This for that. We all own many things, both tangible and intangible - a laptop, a name, sweet kicks, memories, knowledge etc. Collectively, there is an incredible wealth of things that people have. This studio will dive into ways of using technology to facilitate the exchange of said things between people. Get creative! Explore all the inventive possibilities behind how we can connect and what we can exchange.
          </p>
          <p>
            Thought provokers:
            <ul>
              <li>Are there any moments where you are already exchanging something in person? How can we use technology to enhance that experience?</li>
              <li>Are there any times where everyday people would love to make a quick exchange, but don’t because of reasons? How can we design applications to take care of those reasons and make such exchanges happen?</li>
              <li>What are the key qualities of exchange? What’s great about it? What’s not so great about it? When does it work out well? When does it go terribly?</li>
