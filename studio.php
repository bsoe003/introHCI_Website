        <div class="col-lg-12 col-md-12">
          <h3>Resolutions</h3>
          <h5>Studio leader: Nida Hussain</h5>
          <h5>Atkinson 5204, Friday 11:30am - 12:40pm<br>Mandeville B-146, Friday 12:50pm-2:00pm</h5>
          <p>
            Resolutions are the promises we make to ourselves and to others. When trying to keep these promises, technology can be a resource or a liability. An almost infinite supply of entertainment, unplanned interruptions and our own limited time mean that distraction frequently triumphs over even the best of our intentions. This studio will focus on developing technology that supports people as they seek to meaningfully use their time, talent, and resources rather than squander them. The aim is to reduce the friction between intention and outcome. Some questions to consider:
            <ul>
              <li>How do we support deeper thought and reflection in the face of distraction?</li>
              <li>What are the challenges people face in sticking to resolutions and how might they be addressed?</li>
              <li>What motivates people and how do these motivations vary across different personalities, groups and cultures?</li>
            </ul>
            Some readings on <a href="http://www.nytimes.com/2015/12/31/upshot/how-to-stick-to-your-new-years-resolution.html?partner=rss&emc=rss&smid=tw-nytimes&smtyp=cur" target="_blank">making</a> and <a href="http://www.npr.org/sections/13.7/2016/01/04/461879189/we-ve-got-resolutions-all-wrong" target="_blank">keeping</a> resolutions. And, an example, (your apps must be a different concept): <a href="https://chrome.google.com/webstore/detail/stayfocusd/laankejkbhbdhmipfmgcngdelahlfoji?hl=en" target="_blank">Stay Focused</a>.
          </p>
        </div>
